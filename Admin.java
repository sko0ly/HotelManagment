        PreparedStatement ps;
        String connURL = "jdbc:mysql://localhost:3307/hotel_managment?autoReconnect=true&useSSL=false";
	String username = "root";
	String pass = "";
        try{
            conn = DriverManager.getConnection(connURL,username,pass);
            System.out.println("Connected");
            ps = conn.prepareStatement("insert offers set name = ?,start_date = ?,end_date = ?,type=?,price=?");
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());
            ps.setString(3, jTextField3.getText());
            ps.setString(4, jTextField5.getText());
            ps.setString(5, jTextField4.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Offer created!!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            conn.close();
        }catch(SQLException ex){
            System.err.println(ex.getMessage());
        }
