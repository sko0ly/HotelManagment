    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
           
        
        Connection conn=null;
        PreparedStatement ps;
        String connURL = "jdbc:mysql://localhost:3307/hotel_managment?autoReconnect=true&useSSL=false";
	String username = "root";
	String pass = "";
			
        try{
                conn = DriverManager.getConnection(connURL,username,pass);
                System.out.println("Connected");
                ps = conn.prepareStatement("insert rooms set name = ?, start_date = ? , end_date = ?, type = ?, price_per_day = ?");
                ps.setString(1,jTextField1.getText());
                ps.setString(2,(jTextField3.getText()+"/"+jTextField4.getText()+"/"+jTextField5.getText()));
                ps.setString(3,(jTextField6.getText()+"/"+jTextField7.getText()+"/"+jTextField8.getText()));
                ps.setString(4,jTextField2.getText());
                ps.setString(5,jTextField2.getText()+"0");
                ps.executeUpdate();  
                
                
                ps = conn.prepareStatement("select rooms_left from available where type = "+jTextField2.getText());
                ResultSet result = ps.executeQuery();
                if(result.next()){
                    String left = result.getString("rooms_left");
                    int room_left = Integer.parseInt(left);
                    room_left--;
                    ps = conn.prepareStatement("update available set rooms_left =? where type =?");
                    ps.setString(1,Integer.toString(room_left));
                    ps.setString(2,jTextField2.getText());
                    ps.executeUpdate();
                }
        }catch (SQLException ex){
                System.out.println("SQLEception:" + ex.getMessage());
                System.out.println("SQLState" + ex.getSQLState());
                System.out.println("VendorError: " + ex.getErrorCode());
            
        }
    }//GEN-LAST:event_jButton5MouseClicked

