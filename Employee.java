    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        Connection conn=null;
        PreparedStatement ps;
        String connURL = "jdbc:mysql://localhost:3307/hotel_managment?autoReconnect=true&useSSL=false";
	String username = "root";
	String pass = "";
			
        try{
            conn = DriverManager.getConnection(connURL,username,pass);
            System.out.println("Connected");
            ps = conn.prepareStatement("SELECT start_date,end_date FROM rooms WHERE name=?");
            ps.setString(1, jTextField5.getText());
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                jTextField1.setText(rs.getString("start_date"));
                jTextField2.setText(rs.getString("end_date"));
            }
            
            
        }catch (SQLException ex){
            System.out.println("SQLEception:" + ex.getMessage());
            System.out.println("SQLState" + ex.getSQLState());
            System.out.println("VendorError: " + ex.getErrorCode());
            
        }
    }//GEN-LAST:event_jButton2MouseClicked

