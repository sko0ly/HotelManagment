    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
           
               
        Connection conn=null;
        PreparedStatement ps;
        String connURL = "jdbc:mysql://localhost:3307/hotel_managment?autoReconnect=true&useSSL=false";
	String username = "root";
	String pass = "";
			
        try{
            conn = DriverManager.getConnection(connURL,username,pass);
                    
            System.out.println("Connected");
            ps = conn.prepareStatement("SELECT `username`,`password` FROM `login` WHERE `username` = ? AND `password` = ? ");
            ps.setString(1,jTextField1.getText());
            ps.setString(2,String.valueOf(jPasswordField1.getPassword()));
            ResultSet result = ps.executeQuery();
            if(result.next()){
                System.out.println("query ok");
                if((result.getString("username")).equals("admin")){
                    System.out.println("Admin login");
                    Admin a = new Admin();
                    a.setVisible(true);
                    setVisible(false);
                    dispose();
                }else if((result.getString("username")).equals("worker")){
                    System.out.println("Worker login");
                    Employee a = new Employee();
                    a.setVisible(true);
                    setVisible(false);
                    dispose();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Wrong Username or Password");
            }
                
        }catch (SQLException ex){
            System.out.println("SQLEception:" + ex.getMessage());
            System.out.println("SQLState" + ex.getSQLState());
            System.out.println("VendorError: " + ex.getErrorCode());
            
        }
    }//GEN-LAST:event_jButton1MouseClicked

